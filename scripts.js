const sectionAndProjectObserver = new IntersectionObserver(
  entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
      } else {
        // Optional: Remove 'visible' class if you want the animation to reverse on scroll out
        // entry.target.classList.remove('visible');
      }
    });
  },
  { threshold: 0.1 }
);

document.querySelectorAll('.section, .project-card').forEach(element => {
  sectionAndProjectObserver.observe(element);
});

const fadeInOnScrollObserver = new IntersectionObserver(
  entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        fadeInOnScrollObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.5 }
);

document.querySelectorAll('.hero-content .fade-in-on-scroll').forEach(element => {
  fadeInOnScrollObserver.observe(element);
});


